function suma() {
    let n1 = parseFloat(prompt("Dime un numero"))
    let n2 = parseFloat(prompt("Dime otro numero"))
    document.write(n1 + n2)
}
function resta() {
    let n1 = parseFloat(prompt("Dime un numero"))
    let n2 = parseFloat(prompt("Dime otro numero"))
    document.write(n1 - n2)
}
function multiplicar() {
    let n1 = parseFloat(prompt("Dime un numero"))
    let n2 = parseFloat(prompt("Dime otro numero"))
    document.write(n1 * n2)
}
function dividir() {
    let n1 = parseFloat(prompt("Dime un numero"))
    let n2 = parseFloat(prompt("Dime otro numero"))
    document.write(n1/n2)
}
function arrel() {
    let n1 = parseFloat(prompt("Dime un numero"))
    document.write(Math.sqrt(n1))
}